            }

            if (!$has_valid_link) {
                $errors[] = "ÌÇ§ÏõåÎìú '" . clean_input($keyword_item['name'] ?? '') . "'Ïóê Ïú†Ìö®Ìïú ÏÉÅÌíà ÎßÅÌÅ¨Í∞Ä ÏóÜÏäµÎãàÎã§.";
