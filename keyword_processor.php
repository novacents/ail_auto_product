// 6. ÏûÖÎ†•Í∞í ÏïàÏ†Ñ Ï≤òÎ¶¨ Ìï®Ïàò (htmlspecialchars, strip_tags Îì± ÏÇ¨Ïö©)
function clean_input($data) {
