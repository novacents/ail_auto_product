        $errors[] = 'Ïú†Ìö®ÌïòÏßÄ ÏïäÏùÄ Ïπ¥ÌÖåÍ≥†Î¶¨ÏûÖÎãàÎã§.';
    }

    if (empty($data['keywords']) || !is_array($data['keywords'])) {
        $errors[] = 'ÏµúÏÜå ÌïòÎÇòÏùò ÌÇ§ÏõåÎìúÍ∞Ä ÌïÑÏöîÌï©ÎãàÎã§.';
    } else {
        foreach ($data['keywords'] as $index => $keyword_item) {
