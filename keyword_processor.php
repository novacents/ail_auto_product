    $data = htmlspecialchars($data, ENT_QUOTES, 'UTF-8'); // HTML ÌäπÏàò Î¨∏Ïûê Î≥ÄÌôò
    $data = strip_tags($data); // HTML ÌÉúÍ∑∏ Ï†úÍ±∞
